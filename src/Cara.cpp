#include "Cara.h"

Cara::Cara() {
	a=0;
	b=0;
	c=0;
}


////////////////////////////////////////////////////////////////////////////////
Cara::Cara(int vA, int vB,int vC){
	a=vA;
	b=vB;
	c=vC;
}


////////////////////////////////////////////////////////////////////////////////
Cara::Cara(int vA, int vB,int vC,TNormal normal){
	a=vA;
	b=vB;
	c=vC;
	this->normal=normal;
}


////////////////////////////////////////////////////////////////////////////////
int Cara::getA(){
	return a;
}


////////////////////////////////////////////////////////////////////////////////
void Cara::setA( int valor) {
	 a = valor;
}


////////////////////////////////////////////////////////////////////////////////
int Cara::getB()  {
	return b;
}


////////////////////////////////////////////////////////////////////////////////
void Cara::setB( int valor) {
	b = valor;
}


////////////////////////////////////////////////////////////////////////////////
int Cara::getC()  {
	return c;
}


////////////////////////////////////////////////////////////////////////////////
void Cara::setC( int valor) {
	c = valor;
}


////////////////////////////////////////////////////////////////////////////////
TNormal Cara::getNormal(){
	return normal;
}


////////////////////////////////////////////////////////////////////////////////
float Cara::getNormalX(){
	return normal.x;
}


////////////////////////////////////////////////////////////////////////////////
float Cara::getNormalY(){
	return normal.y;
}


////////////////////////////////////////////////////////////////////////////////
float Cara::getNormalZ(){
	return normal.z;
}


////////////////////////////////////////////////////////////////////////////////
void Cara::setNormal(TNormal normal){
	this->normal=normal;
}


Cara::~Cara() {
}
