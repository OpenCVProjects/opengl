#include "Punto3D.h"

Punto3D::Punto3D() {
	x=0;
	y=0;
	z=0;

}


////////////////////////////////////////////////////////////////////////////////
Punto3D::Punto3D(int x,int y, int z) {
	this->x=x;
	this->y=y;
	this->z=z;
}


////////////////////////////////////////////////////////////////////////////////
int Punto3D::getX()  {
		return x;
}


////////////////////////////////////////////////////////////////////////////////
void Punto3D::setX(int x) {
	this->x = x;
}


////////////////////////////////////////////////////////////////////////////////
int Punto3D::getY()  {
	return y;
}


////////////////////////////////////////////////////////////////////////////////
void Punto3D::setY(int y) {
	this->y = y;
}


////////////////////////////////////////////////////////////////////////////////
int Punto3D::getZ()  {
	return z;
}


////////////////////////////////////////////////////////////////////////////////
void Punto3D::setZ(int z) {
	this->z = z;
}


////////////////////////////////////////////////////////////////////////////////
Punto3D::~Punto3D() {
}
